def create_pdf() -> None:
    """Generate a 10-page Hindi PDF."""
    if not FONT_PATH.exists():
        print(f"ERROR: Font not found at {FONT_PATH}")
        print("Download Noto Sans Devanagari to fonts/NotoSansDevanagari.ttf")
        sys.exit(1)

    font = fitz.Font(fontfile=str(FONT_PATH))
    doc = fitz.open()

    for page_num, (title, body) in enumerate(PAGES):
        page = doc.new_page(width=595, height=842)  # A4
        tw = fitz.TextWriter(page.rect)

        # Title
        y = 60
        if page_num == 0:
            # Title page — centered, larger
            tw.append((50, y), title, font=font, fontsize=22)
            y += 60
            tw.append((50, y), "— परीक्षण पुस्तिका —", font=font, fontsize=14)
            y += 50
        else:
            tw.append((50, y), title, font=font, fontsize=16)
            y += 40

        # Body text — wrap manually at ~80 chars per line
        line_height = 22
        fontsize = 12
        margin_left = 50
        margin_right = 545
        max_width = margin_right - margin_left

        words = body.split()
        line = ""
        for word in words:
            test_line = f"{line} {word}".strip() if line else word
            # Estimate width using font
            tl = font.text_length(test_line, fontsize=fontsize)
            if tl > max_width and line:
                tw.append((margin_left, y), line, font=font, fontsize=fontsize)
                y += line_height
                line = word
                if y > 790:
                    break
            else:
                line = test_line
        if line and y <= 790:
            tw.append((margin_left, y), line, font=font, fontsize=fontsize)

        # Page number
        tw.append((280, 820), f"— {page_num + 1} —", font=font, fontsize=10)

        tw.write_text(page)

    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    doc.save(str(OUTPUT_PATH))
    doc.close()

    # Verify
    verify_doc = fitz.open(str(OUTPUT_PATH))
    print(f"✅ Created {OUTPUT_PATH} — {len(verify_doc)} pages, {OUTPUT_PATH.stat().st_size} bytes")
    for i in range(len(verify_doc)):
        text = verify_doc[i].get_text().strip()
        word_count = len(text.split())
        print(f"  Page {i + 1}: {word_count} words")
    verify_doc.close()


if __name__ == "__main__":
    create_pdf()
